#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	i;
	char	*ret;

	i = 0;
	while (str[i] != '\0')
		i++;
	ret = (char *)malloc(sizeof(char) * (i + 1));
	if (!ret)
		return (NULL);
	i = 0;
	while (str[i] != '\0')
	{
		ret[i] = str[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
